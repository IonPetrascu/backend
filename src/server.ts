import 'dotenv/config';
import app from './app';

// Читаем порт из переменных окружения (из .env файла).
// Если переменной нет — используем 3000 как запасной вариант.
// process.env — это объект со всеми переменными окружения (как import.meta.env на фронте с Vite).
const PORT = process.env.PORT || 3000;

// app.listen() — вот здесь сервер реально запускается и начинает слушать входящие запросы.
// Первый аргумент: порт.
// Второй аргумент: callback, который вызывается когда сервер успешно стартовал.
app.listen(PORT, () => {
  console.log(`Сервер запущен на http://localhost:${PORT}`);
});
